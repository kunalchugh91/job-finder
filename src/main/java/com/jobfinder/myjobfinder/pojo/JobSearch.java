package com.jobfinder.myjobfinder.pojo;



public class JobSearch {
	
	
	public String name;
	
	
	public String phone;
	
	
	public String email;
	
	
	public String company;	
	
	public int jobID;
	

	public String jobTitle;
	

	public String location;
	

	public String jobCategory;
	
	
	public String employmentType; //Full-time, Part-time, Contractor,Temporary. 
	
	
	
	public String jobDescription;
	
	
	
	public int rangeAmt;
	
	
	public String currency;
	

	public String term; //yearly, monthly etc
	

	public boolean plusCommission;
	
	public String hasApplied;
	
	


	public String getHasApplied() {
		return hasApplied;
	}


	public void setHasApplied(String hasApplied) {
		this.hasApplied = hasApplied;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getPhone() {
		return phone;
	}


	public void setPhone(String phone) {
		this.phone = phone;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}


	public String getCompany() {
		return company;
	}


	public void setCompany(String company) {
		this.company = company;
	}


	public int getJobID() {
		return jobID;
	}


	public void setJobID(int jobID) {
		this.jobID = jobID;
	}


	public String getJobTitle() {
		return jobTitle;
	}


	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}


	public String getLocation() {
		return location;
	}


	public void setLocation(String location) {
		this.location = location;
	}


	public String getJobCategory() {
		return jobCategory;
	}


	public void setJobCategory(String jobCategory) {
		this.jobCategory = jobCategory;
	}


	public String getEmploymentType() {
		return employmentType;
	}


	public void setEmploymentType(String employmentType) {
		this.employmentType = employmentType;
	}


	public String getJobDescription() {
		return jobDescription;
	}


	public void setJobDescription(String jobDescription) {
		this.jobDescription = jobDescription;
	}


	public int getRangeAmt() {
		return rangeAmt;
	}


	public void setRangeAmt(int rangeAmt) {
		this.rangeAmt = rangeAmt;
	}


	public String getCurrency() {
		return currency;
	}


	public void setCurrency(String currency) {
		this.currency = currency;
	}


	public String getTerm() {
		return term;
	}


	public void setTerm(String term) {
		this.term = term;
	}


	public boolean isPlusCommission() {
		return plusCommission;
	}


	public void setPlusCommission(boolean plusCommission) {
		this.plusCommission = plusCommission;
	}	
	
	
	

}
